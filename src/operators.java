public class operators {
    public static void main(String[] args) {
        //https://www.w3schools.com/java/java_operators.asp
        //addition assignment operator (+=) adds a value to a variable:
        int x = 10;
        x += 5;
        System.out.println(x);

        //Operator   EX         Same as
        //  = 	    x = 5 	    x = 5
        //  += 	    x += 3 	    x = x + 3
        //  -= 	    x -= 3 	    x = x - 3
        //  *= 	    x *= 3 	    x = x * 3
        //  /= 	    x /= 3 	    x = x / 3
        //  %= 	    x %= 3 	    x = x % 3
        //  &= 	    x &= 3 	    x = x & 3
        //  |= 	    x |= 3 	    x = x | 3
        //  ^= 	    x ^= 3 	    x = x ^ 3
        //  >>= 	x >>= 3 	x = x >> 3
        //  <<= 	x <<= 3 	x = x << 3
    }
}
